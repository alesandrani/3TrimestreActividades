package actividad13;

public class Calculadora {
	/**
	 * Es un metodo que hace la suma de los numeros pasados por parametro
	 * @param n1 representa el primer numero pasado por parametro
	 * @param n2 representa segundo numero pasado por parametro
	 * @return devuelve como resultado la suma de los numeros pasados por parametro
	 */
	public static double sumar(double n1, double n2) {
		
		return n1 + n2;
	}
	/**
	 * Es un metodo que hace la resta de los numeros pasados por parametro
	 * @param n1 representa el primer numero pasado por parametro
	 * @param n2 representa segundo numero pasado por parametro
	 * @return devuelve como resultado la resta de los numeros pasados por parametro
	 */
	public static double restar(double n1, double n2) {
		
		return n1 - n2;
	}
	/**
	 * Es un metodo que hace la multiplicacion de los numeros pasados por parametro
	 * @param n1 representa el primer numero pasado por parametro
	 * @param n2  representa segundo numero pasado por parametro
	 * @return devuelve como resultado la multiplicacion de los numeros pasados por parametro
	 */
	public static double multiplicar(double n1, double n2) {
	
	return n1 * n2;
	
}
	/**
	 * Es un metodo que hace la division de los numeros pasados por parametro
	 * @param n1 representa el primer numero pasado por parametro
	 * @param n2 representa segundo numero pasado por parametro
	 * @return devuelve como resultado de la diviison de los numeros pasados por parametro
	 */
	public static double dividir(double n1, double n2) {
		
		return n1 / n2;
	}
}
